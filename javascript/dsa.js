function toggle1() {
    Array.from(document.getElementsByClassName("road")).forEach((e) => {
        e.style.display = "flex";
    })

    Array.from(document.getElementsByClassName("youtube")).forEach((e) => {
        e.style.display = "none";
    })

    Array.from(document.getElementsByClassName("paid")).forEach((e) => {
        e.style.display = "none";
    })

    Array.from(document.getElementsByClassName("documentation")).forEach((e) => {
        e.style.display = "none";
    })

    Array.from(document.getElementsByClassName("practice")).forEach((e) => {
        e.style.display = "none";
    })
}

